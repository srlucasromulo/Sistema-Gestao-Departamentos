class Departamento:
	def __init__(self, nome):
		self.nome = nome
		self.cursos = []
		self.funcionarios = []
		self.documentos = []

	def getNome():
		return self.nome

	def setNome(nome):
		self.nome = nome

	def getCursos():
		return self.cursos

	def setCursos(cursos):
		self.cursos = cursos

	def getFuncionarios():
		return self.funcionarios

	def setFuncionarios(funcionarios):
		self.funcionarios = funcionarios

	def getDocumentos():
		return self.documentos

	def setDocumentos(documentos):
		self.documentos = documentos
